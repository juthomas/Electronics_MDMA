uint8_t	*led_gimp_compute(uint8_t luminosity , uint8_t* item)
{
	uint8_t *pixels = 0;
	pixels = (uint8_t*)malloc(sizeof(uint8_t) * (64 * 3 + 1));
	bzero(pixels, sizeof(uint8_t) * (64 * 3 + 1));
	 memcpy(pixels, item, 64*4);
     ;
	uint8_t r;
	uint8_t g;
	uint8_t b;

	for (int i = 0; i < 64; i++)
	{
		r = pixels[i * 3] * luminosity / 100;
		g = pixels[i * 3 + 1] * luminosity / 100;
		b = pixels[i * 3 + 2] * luminosity / 100;
		pixels[i * 3] = g;
		pixels[i * 3 + 1] = r;
		pixels[i * 3 + 2] = b;
	}

	return (pixels);
}


uint8_t	*led_gimp_compute_colorize(uint8_t luminosity , uint8_t* item, uint8_t red, uint8_t green, uint8_t blue)
{
	uint8_t *pixels = 0;
	pixels = (uint8_t*)malloc(sizeof(uint8_t) * (64 * 3 + 1));
	bzero(pixels, sizeof(uint8_t) * (64 * 3 + 1));
	 memcpy(pixels, item, 64*4);
     ;
	uint8_t r;
	uint8_t g;
	uint8_t b;

	for (int i = 0; i < 64; i++)
	{
		r = pixels[i * 3] * luminosity / 100 * red / 100;
		g = pixels[i * 3 + 1] * luminosity / 100 * green / 100;
		b = pixels[i * 3 + 2] * luminosity / 100 * blue / 100;
		pixels[i * 3] = g;
		pixels[i * 3 + 1] = r;
		pixels[i * 3 + 2] = b;
	}

	return (pixels);
}

